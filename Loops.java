public class Loops {
    public static void main(String[] args) {
        for (int i = 7; i < 23; i += 3) {
            System.out.println("Step: " + i);
        }
        System.out.println();
        for (int i = 10; i > -1; i -= 1) {
            System.out.println("Step: " + i);
        }
        System.out.println();
        for (int i = 0; i < 6; i++) {
            System.out.println("Step: " + i);
        }
    }
}
